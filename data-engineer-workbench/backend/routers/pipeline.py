def _topological_sort(nodes: list, edges: list) -> list:
    adj = {n.id: [] for n in nodes}
    in_deg = {n.id: 0 for n in nodes}
    for e in edges:
        adj[e.source].append(e.target)
        in_deg[e.target] += 1

    queue = [n for n in nodes if in_deg[n.id] == 0]
    result = []
    while queue:
        node = queue.pop(0)
        result.append(node)
        for neighbor in adj[node.id]:
            in_deg[neighbor] -= 1
            if in_deg[neighbor] == 0:
                next_node = next((n for n in nodes if n.id == neighbor), None)
                if next_node:
                    queue.append(next_node)
    return result or nodes


def _get_prev_var(node_id: str, edges: list, node_vars: dict) -> str:
    for e in edges:
        if e.target == node_id and e.source in node_vars:
            return node_vars[e.source]
    return "df"


