from fastapi import APIRouter
from pydantic import BaseModel
from typing import Any, Optional
import time
import random
from datetime import datetime

router = APIRouter()


class PipelineNode(BaseModel):
    id: str
    type: str
    config: dict[str, Any]
    position: dict[str, float]


class PipelineEdge(BaseModel):
    source: str
    target: str


class PipelineRunRequest(BaseModel):
    nodes: list[PipelineNode]
    edges: list[PipelineEdge]


class CodeGenRequest(BaseModel):
    nodes: list[PipelineNode]
    edges: list[PipelineEdge]


@router.post("/pipeline/run")
def run_pipeline(body: PipelineRunRequest):
    steps = []
    input_rows = random.randint(40000, 60000)
    current_rows = input_rows

    node_order = _topological_sort(body.nodes, body.edges)

    for node in node_order:
        start_time = time.time()
        step_duration = round(random.uniform(0.1, 2.5), 3)
        output_rows = current_rows

        log_messages = []

        if node.type == "source":
            table = node.config.get("table", "fact_orders")
            log_messages = [
                f"Connecting to source: {table}",
                f"Reading schema: {random.randint(5, 15)} columns detected",
                f"Extracted {current_rows:,} rows",
            ]
        elif node.type == "filter":
            condition = node.config.get("condition", "status = 'completed'")
            dropped = random.randint(int(current_rows * 0.1), int(current_rows * 0.4))
            output_rows = current_rows - dropped
            log_messages = [
                f"Applying filter: WHERE {condition}",
                f"Filtered out {dropped:,} rows",
                f"Remaining: {output_rows:,} rows",
            ]
        elif node.type == "join":
            right_table = node.config.get("right_table", "dim_customer")
            join_type = node.config.get("join_type", "LEFT JOIN")
            extra = random.randint(0, 500)
            output_rows = current_rows + extra
            log_messages = [
                f"Executing {join_type} with {right_table}",
                f"Join key: {node.config.get('on', 'customer_id')}",
                f"Output: {output_rows:,} rows (+{extra} from join expansion)",
            ]
        elif node.type == "aggregate":
            group_by = node.config.get("group_by", "category")
            agg_fn = node.config.get("aggregation", "SUM(revenue)")
            output_rows = random.randint(10, 500)
            log_messages = [
                f"Grouping by: {group_by}",
                f"Aggregating: {agg_fn}",
                f"Reduced to {output_rows:,} groups",
            ]
        elif node.type == "rename":
            mappings = node.config.get("mappings", {})
            log_messages = [
                f"Renaming {len(mappings)} columns",
                f"Applied: {', '.join([f'{k}→{v}' for k, v in list(mappings.items())[:3]])}",
            ]
        elif node.type == "sink":
            dest = node.config.get("destination", "gold.fact_daily_revenue")
            mode = node.config.get("write_mode", "overwrite")
            log_messages = [
                f"Writing {current_rows:,} rows to {dest}",
                f"Write mode: {mode}",
                f"Created partition key: {node.config.get('partition_by', 'date')}",
                f"Committed successfully",
            ]
        else:
            log_messages = [f"Executing node: {node.type}"]

        steps.append({
            "node_id": node.id,
            "node_type": node.type,
            "input_rows": current_rows,
            "output_rows": output_rows,
            "duration_seconds": step_duration,
            "logs": log_messages,
            "status": "success",
            "schema_preview": _get_sample_schema(node.type),
            "data_preview": _get_sample_rows(node.type, output_rows),
        })
        current_rows = output_rows

    total_duration = sum(s["duration_seconds"] for s in steps)
    return {
        "run_id": f"RUN-{datetime.now().strftime('%Y%m%d%H%M%S')}",
        "status": "success",
        "total_duration_seconds": round(total_duration, 3),
        "input_rows": input_rows,
        "output_rows": current_rows,
        "steps": steps,
        "completed_at": datetime.now().isoformat(),
    }


@router.post("/pipeline/generate-code")
def generate_code(body: CodeGenRequest):
    lines = [
        "import pandas as pd",
        "import sqlite3",
        "from datetime import datetime",
        "",
        "# Generated by Senior Data Engineer Workbench",
        f"# Pipeline generated at {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        "",
        "def run_pipeline():",
        "    print('Starting pipeline execution...')",
        "    conn = sqlite3.connect('workbench.db')",
        "",
    ]

    node_vars = {}
    node_order = _topological_sort(body.nodes, body.edges)

    for i, node in enumerate(node_order):
        var_name = f"df_{node.type}_{i}"
        node_vars[node.id] = var_name

        if node.type == "source":
            table = node.config.get("table", "fact_orders")
            lines += [
                f"    # Step {i+1}: Extract from source",
                f"    {var_name} = pd.read_sql('SELECT * FROM {table}', conn)",
                f"    print(f'Extracted {{len({var_name}):,}} rows from {table}')",
                "",
            ]
        elif node.type == "filter":
            cond = node.config.get("pandas_condition", "df['status'] == 'completed'")
            prev_var = _get_prev_var(node.id, body.edges, node_vars)
            lines += [
                f"    # Step {i+1}: Filter rows",
                f"    {var_name} = {prev_var}[{cond}].copy()",
                f"    print(f'Filter: {{len({var_name}):,}} rows remaining')",
                "",
            ]
        elif node.type == "join":
            right = node.config.get("right_table", "dim_customer")
            on = node.config.get("on", "customer_id")
            how = node.config.get("join_type", "left").lower().replace(" join", "")
            prev_var = _get_prev_var(node.id, body.edges, node_vars)
            lines += [
                f"    # Step {i+1}: Join with {right}",
                f"    dim = pd.read_sql('SELECT * FROM {right}', conn)",
                f"    {var_name} = {prev_var}.merge(dim, on='{on}', how='{how}')",
                f"    print(f'After join: {{len({var_name}):,}} rows')",
                "",
            ]
        elif node.type == "aggregate":
            group_by = node.config.get("group_by", "category")
            agg_col = node.config.get("agg_column", "revenue")
            agg_fn = node.config.get("agg_function", "sum")
            prev_var = _get_prev_var(node.id, body.edges, node_vars)
            lines += [
                f"    # Step {i+1}: Aggregate",
                f"    {var_name} = {prev_var}.groupby('{group_by}')['{agg_col}'].{agg_fn}().reset_index()",
                f"    {var_name}.columns = ['{group_by}', '{agg_fn}_{agg_col}']",
                f"    print(f'Aggregated to {{len({var_name}):,}} groups')",
                "",
            ]
        elif node.type == "rename":
            mappings = node.config.get("mappings", {"revenue": "total_revenue"})
            prev_var = _get_prev_var(node.id, body.edges, node_vars)
            lines += [
                f"    # Step {i+1}: Rename columns",
                f"    {var_name} = {prev_var}.rename(columns={mappings})",
                "",
            ]
        elif node.type == "sink":
            dest = node.config.get("destination", "gold_daily_revenue")
            mode = "replace" if node.config.get("write_mode", "overwrite") == "overwrite" else "append"
            prev_var = _get_prev_var(node.id, body.edges, node_vars)
            lines += [
                f"    # Step {i+1}: Load to destination",
                f"    {prev_var}['updated_at'] = datetime.now().isoformat()",
                f"    {prev_var}.to_sql('{dest}', conn, if_exists='{mode}', index=False)",
                f"    print(f'Loaded {{len({prev_var}):,}} rows to {dest}')",
                "",
            ]

    lines += [
        "    conn.close()",
        "    print('Pipeline completed successfully!')",
        "",
        "",
        "if __name__ == '__main__':",
        "    run_pipeline()",
    ]

    return {"code": "\n".join(lines), "language": "python"}


